# Defining Functions in Python:

# Functions are reusable blocks of code that perform specific tasks. They help you organize your code, make it more readable, and avoid repetition.

# 1. Basic Function Definition:

#   - Use the `def` keyword followed by the function name and parentheses.
#   - The function body is indented.
#   - The `return` statement specifies the value the function returns (optional).

#   - Example:

def greet(name):
    """Greets the user with a personalized message."""
    print(f"Hello, {name}!")

greet("Alice")
#output:-
# Hello, Alice!

