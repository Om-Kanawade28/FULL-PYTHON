
# Variables are containers for storing data values.
# Data types are classifications of data that determine the type of values a variable can hold.

# Example:
# Integer
age = 25
# String
name = "John Doe"
# Float
height = 1.85
# Boolean
is_student = True

# Print the values of the variables
print(age)
print(name)
print(height)
print(is_student)

# Output:
# 25
# John Doe
# 1.85
# True

# Data Types in Python:
# 1. Integers (int): Whole numbers without decimal points.
# 2. Floats (float): Numbers with decimal points.
# 3. Strings (str): Sequences of characters enclosed in single or double quotes.
# 4. Booleans (bool): Logical values, either True or False.
# 5. Lists (list): Ordered collections of items enclosed in square brackets.
# 6. Tuples (tuple): Ordered, immutable collections of items enclosed in parentheses.
# 7. Dictionaries (dict): Unordered collections of key-value pairs enclosed in curly braces.
# 8. Sets (set): Unordered collections of unique items enclosed in curly braces.

# Example:
# List
fruits = ["apple", "banana", "orange"]
# Tuple
coordinates = (10, 20)
# Dictionary
person = {"name": "John", "age": 25}
# Set
unique_numbers = {1, 2, 3, 4, 5}

# Print the values of the variables
print(fruits)
print(coordinates)
print(person)
print(unique_numbers)

# Output:
# ['apple', 'banana', 'orange']
# (10, 20)
# {'name': 'John', 'age': 25}
# {1, 2, 3, 4, 5}

